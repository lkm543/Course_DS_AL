<h2>Chapter 08 字元與字串</h2>

<h2>大綱</h2>
<p>如果需要一次開啟大筆資料來使用，像是為全社區的住戶開個帳戶之類的，這時候傳統的變數宣告會變得非常麻煩，需要一個個去宣告它太沒有效率，所以這時候可以使用陣列，一次開出大量的資料來讓我們使用，同時可以搭配維度的概念，讓陣列擴展成多維來更方便我們利用。此外字串也可以視為字元所構成的陣列，也擁有陣列的特性。<br>(本章預計授課時間：30-40 Mins)</p> 

