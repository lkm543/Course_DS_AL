<h2>大綱</h2>
<p>函式的意思是把一段會重複用到的程式碼獨立寫出來，每當我們會需要使用的時候便直接呼叫該函式即可，可避免同樣的程式碼重複出現，同時增加程式碼的可讀性與可維護性。<br>(本章預計授課時間：140-160 Mins)</p> 

