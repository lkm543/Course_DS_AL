<h2>Chapter 05 例外判斷</h2>

<h2>大綱</h2>
<p>當程式運行中發生錯誤時有可能導致程式自動關閉，甚至讓電腦當機，因此如何處理程式運行中可能發生的錯誤是程式設計師必學的環節之一，而其中錯誤又可以分成可預期或不可預期，可預期的錯誤必須去處理或修正，而不可預期的錯誤則盡量避免讓程式直接關閉。<br>(本章預計授課時間：55-60 Mins)</p> 

